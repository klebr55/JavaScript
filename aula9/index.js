// 1. Números (Number):
//    - Representa valores numéricos, incluindo inteiros e números de ponto flutuante.
let numero = 42;

// 2. Strings:
//    - Sequência de caracteres, representadas por aspas simples ('') ou duplas ("").
let texto = "Olá, mundo!";

// 3. Booleanos (Boolean):
//    - Representa um valor verdadeiro (true) ou falso (false).
let verdadeiro = true;
let falso = false;

// 4. Undefined:
//    - Representa um valor indefinido.
let indefinido;

// 5. Null:
//    - Representa um valor nulo, indicando a ausência de valor ou um valor vazio.
let nulo = null;

// 6. Symbol:
//    - Representa um valor único e imutável, frequentemente usado como identificadores únicos de propriedades de objetos.
let simbolo = Symbol("descrição");

// 7. BigInt:
//    - Representa números inteiros de precisão arbitrária, permitindo representar inteiros maiores que 2^53 - 1.
let grandeNumero = 9007199254740991n;

